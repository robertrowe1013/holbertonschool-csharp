using System;

/// <summary>
///  Vectormath
/// </summary>
class VectorMath
{
    public static double[] Add(double[] vector1, double[] vector2)
    {
        double[] v = new double[] {-1};
        if (vector1.Length == 2 && vector2.Length == 2)
        {
            return (v);
        }
        else if (vector1.Length == 3 && vector2.Length == 3)
        {
            return (v);
        }
        else
        {
            return (v);
        }
    }
}
